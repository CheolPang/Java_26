package extend.calc;

public class Area { // 부모 클래스, DTO
	private double base; // 밑변
	private double height; // 높이

	public double getBase() {
		return base;
	}

	public void setBase(double base) {
		this.base = base;
	}

	public double getHeight() {
		return height;
	}

	public void setHeight(double height) {
		this.height = height;
	}

}
