package variable;

public class Exam01 {
	public static void main(String[] args) {
		int num1 = 50;
		double num2 = 187.5;
		char ch = 'B';
		boolean b = true;
		
		System.out.println("num1: "+num1);
		System.out.printf("num1: %d\n", num1);
		System.out.printf("num2: %f\n", num2);
		System.out.printf("char: %c\n", ch);
		System.out.printf("boolean: %b\n", b);
		System.out.println();
		System.out.println();
		System.out.printf("boolean: %b\n", b );
	}

}
